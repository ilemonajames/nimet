<h2 id="item-description__features-include">FEATURES INCLUDE</h2>

<h3 id="item-description__session-management">Course and Session Management</h3>
<p>
    Create multiple online courses, training sessions or a combination of both! Configure each based on your needs. Optionally enable payment collection for enrollment. The following payment methods are available: Paypal, Stripe, 2Checkout, Ipay, Payfast,PayTabs,PayUMoney, Paystack, Rave and others. TrainEasy supports all currencies.
</p>


<h3 id="item-description__interactive-dashboard">Class Management</h3>
<p>Create multiple reusable classes and assign them to sessions or courses to create your online courses or session calendar. You can also assign instructors to courses, sessions and classes and schedule upcoming class reminders for students and instructors! </p>

<h3 id="item-description__class-management">Online Tests/Assessments</h3>
<p>
    TrainEasy comes with a powerful online testing/assessment feature. Save yourself the paperwork and switch to TrainEasy’s computer based tests! Tests can be created with multiple options such as Time limit, Session restriction, Attempt limits etc. Students will see their results immediately after completing the test.
</p>

<h3 id="item-description__student-management">Student Management</h3>
<p>Students can register on the frontend of the application or can be added manually through the backend. TrainEasy also features a form builder that enables you to add as many registration fields and attributes to a student as you require.</p>

<h3 id="item-description__role-management">Role Management</h3>
<p>Traineasy supports two major roles: Student & Admin. Admins can be subdivided into multiple roles. Out of the box, you have Super Administrator, Administrator and Instructor. TrainEasy enables you to create new roles with associated permissions for each role.</p>

<h3 id="item-description__reminders">Email & SMS Reminders</h3>
<p>TrainEasy can automatically send reminders to Students and Instructors about upcoming classes, test and homework.</p>


<h3 id="item-description__attendance-management">Attendance Management</h3>
<p>
    TrainEasy make it easy for you to monitor and log attendance records for your students. There are multiple attendance logging methods available that enable you set attendance either online or offline.
</p>


